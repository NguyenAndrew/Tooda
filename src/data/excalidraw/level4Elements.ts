// Helper to create a labeled rectangle element pair
function makeBox(id: string, x: number, y: number, w: number, h: number, text: string, bg: string) {
  return [
    {
      id,
      type: 'rectangle' as const,
      x, y, width: w, height: h,
      angle: 0,
      strokeColor: '#1e1e1e',
      backgroundColor: bg,
      fillStyle: 'solid' as const,
      strokeWidth: 2,
      strokeStyle: 'solid' as const,
      roughness: 1,
      opacity: 100,
      groupIds: [],
      frameId: null,
      roundness: { type: 3 as const },
      seed: 1,
      version: 1,
      versionNonce: 1,
      isDeleted: false,
      boundElements: [{ type: 'text' as const, id: id + '-label' }],
      updated: 1,
      link: null,
      locked: false,
    },
    {
      id: id + '-label',
      type: 'text' as const,
      x: x,
      y: y + (h - 20) / 2,
      width: w,
      height: 20,
      angle: 0,
      strokeColor: '#1e1e1e',
      backgroundColor: 'transparent' as const,
      fillStyle: 'solid' as const,
      strokeWidth: 1,
      strokeStyle: 'solid' as const,
      roughness: 1,
      opacity: 100,
      groupIds: [],
      frameId: null,
      roundness: null,
      seed: 2,
      version: 1,
      versionNonce: 1,
      isDeleted: false,
      boundElements: null,
      updated: 1,
      link: null,
      locked: false,
      text,
      fontSize: 16,
      fontFamily: 1 as const,
      textAlign: 'center' as const,
      verticalAlign: 'middle' as const,
      containerId: id,
      originalText: text,
      autoResize: true,
      lineHeight: 1.25 as const,
    },
  ];
}

function makeArrow(id: string, fromId: string, toId: string, x: number, y: number, dx: number, dy: number, label?: string) {
  const elements = [
    {
      id,
      type: 'arrow' as const,
      x, y,
      width: Math.abs(dx),
      height: Math.abs(dy),
      angle: 0,
      strokeColor: '#1e1e1e',
      backgroundColor: 'transparent' as const,
      fillStyle: 'solid' as const,
      strokeWidth: 2,
      strokeStyle: 'solid' as const,
      roughness: 1,
      opacity: 100,
      groupIds: [],
      frameId: null,
      roundness: { type: 2 as const },
      seed: 3,
      version: 1,
      versionNonce: 1,
      isDeleted: false,
      boundElements: label ? [{ type: 'text' as const, id: id + '-label' }] : null,
      updated: 1,
      link: null,
      locked: false,
      points: [[0, 0], [dx, dy]],
      lastCommittedPoint: null,
      startBinding: { elementId: fromId, focus: 0, gap: 1 },
      endBinding: { elementId: toId, focus: 0, gap: 1 },
      startArrowhead: null,
      endArrowhead: 'arrow' as const,
      elbowed: false,
    },
  ];
  if (label) {
    elements.push({
      id: id + '-label',
      type: 'text' as const,
      x: x + dx / 2 - 60,
      y: y + dy / 2 - 12,
      width: 120,
      height: 20,
      angle: 0,
      strokeColor: '#1e1e1e',
      backgroundColor: '#ffffff' as const,
      fillStyle: 'solid' as const,
      strokeWidth: 1,
      strokeStyle: 'solid' as const,
      roughness: 1,
      opacity: 100,
      groupIds: [],
      frameId: null,
      roundness: null,
      seed: 4,
      version: 1,
      versionNonce: 1,
      isDeleted: false,
      boundElements: null,
      updated: 1,
      link: null,
      locked: false,
      text: label,
      fontSize: 12,
      fontFamily: 1 as const,
      textAlign: 'center' as const,
      verticalAlign: 'middle' as const,
      containerId: id,
      originalText: label,
      autoResize: true,
      lineHeight: 1.25 as const,
    });
  }
  return elements;
}


